public class CharDataType {
    static void main(String[] args) {
        char ch = 'A';
        int x = ch;
        System.out.println(x);
        System.out.println(ch);

        char pawan = 'a';
        int y = (int)pawan;  // explicit typecasting
        System.out.println(y);
    }
}